'''
   셔뱅(shebang) = 현재 파일을 실행해 줄 프로그램을 지정할 때 사용한다. 셔뱅에 파이썬 경로를
                       지정해 놓으면 해당 파이썬으로 .py파일을 실행 Linux일 때 적용됨
'''
print( 'Hello, World!!!')

# 파이썬 숫자 : 정수 (integer)
#                  실수(floating-point)
#                  복소수(complex number)
print(1 + 1)
print(1 - 1)
print(1 * 1)
print(5 / 2) # 나누셈 결과 : 실수
print(5 // 2) # //를 쓰면 정수만 뽑아서 나옴, 버림 나눗셈( floor division ), 소수점 이하 버림, 항상 .0으로 나옴

print()
print(5.5 // 2)
print(5.5 / 2)
print(4 // 2.0)
print(4 / 2.0)
print( 4.1 // 2.1 )
print( 4.1 / 2.1 )
# 위에꺼 비교해보기

print()
print( 5 % 2 ) # 나머지 연산 2로 나눠서 나머지만 나타냄
print()
print( 2 ** 10 ) # 거듭제곱 2를 10번 곱한것

























